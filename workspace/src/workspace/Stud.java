package workspace;

public class Stud {

	public static void main(String[] args) 
	{
		
		int a = 12, b = -8;
		
		System.out.printf("%d << %d = %d\n", a, 2, (a << 2));
		System.out.printf("%d >> %d = %d\n", a, 2, (a >> 2)); 
		
		System.out.printf("%d >> %d = %d\n", b, 2, (b >> 2)); 
		System.out.printf("%d >>> %d = %d\n", b, 2, (b >> 2)); 
		
	}

}
